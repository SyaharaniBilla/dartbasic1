void main() {
  var a = 5;
  var b = 10;

  var multiplicationResult = a * b;
  var additionResult = a + b;
  var subtractionResult = a - b;
  var divisionResult = a / b;

  print('a * b = $multiplicationResult');
  print('a + b = $additionResult');
  print('a - b = $subtractionResult');
  print('a / b = $divisionResult');
}
